test('CLI reports conflicts when duplicate loci disagree', () => {
  const scriptPath = path.join(__dirname, '..', 'convert-dnaera.js');
  const tempDir = fs.mkdtempSync(path.join(os.tmpdir(), 'dnaera-to-23andme-conflict-'));
  const inputPath = path.join(tempDir, 'conflict.txt');
  fs.writeFileSync(
    inputPath,
    [
      '[Header]',
      'Content,,GSAMD-24v3-0-EA_20034606_A1.bpm',
      '[Data]',
      'Sample Name,SNP Name,Chr,Position,Allele1 - Plus,Allele2 - Plus',
      '1,19:45411941,19,45411941,T,C',
      '1,rs429358,19,45411941,C,C',
      '1,rs429358,19,45411941,T,T'
    ].join('\n'),
    'utf8'
  );

  const result = spawnSync(process.execPath, [scriptPath, inputPath, '--stdout'], { encoding: 'utf8' });
  assert.equal(result.status, 0, result.stderr);
  assert.match(result.stderr, /Conflicts \(1\):/);
  assert.match(result.stderr, /kept CC from rs429358/);
});
