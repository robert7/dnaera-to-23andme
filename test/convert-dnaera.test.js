const test = require('node:test');
const assert = require('node:assert/strict');
const fs = require('node:fs');
const os = require('node:os');
const path = require('node:path');
const { spawnSync } = require('node:child_process');

const converter = require('../convert-dnaera.js');
const mapping = require('../mapping/getbased-dnaera-map.json');

const sampleInput = fs.readFileSync(path.join(__dirname, '..', 'samples', 'dnaera-mini.txt'), 'utf8');

test('parseArgs derives default output path and respects flags', () => {
  const parsed = converter.parseArgs(['samples/input.txt', '--mapping', 'mapping/custom.json']);
  assert.equal(parsed.inputPath, 'samples/input.txt');
  assert.equal(parsed.outputPath, 'samples/input.23andme.txt');
  assert.equal(parsed.mappingPath, 'mapping/custom.json');
  assert.equal(parsed.stdout, false);

  const stdoutParsed = converter.parseArgs(['samples/input.txt', '--stdout']);
  assert.equal(stdoutParsed.stdout, true);
  assert.equal(stdoutParsed.outputPath, null);
});

test('parseArgs rejects unexpected extra arguments', () => {
  assert.throws(
    () => converter.parseArgs(['input.txt', 'output.txt', 'extra.txt']),
    /Unexpected argument: extra\.txt/
  );
});

test('parseCsvLine handles quoted commas and escaped quotes', () => {
  assert.deepEqual(
    converter.parseCsvLine('alpha,"beta,gamma","delta ""quoted"""'),
    ['alpha', 'beta,gamma', 'delta "quoted"']
  );
});

test('toCsvLine escapes commas and quotes', () => {
  assert.equal(
    converter.toCsvLine(['alpha', 'beta,gamma', 'delta "quoted"']),
    'alpha,"beta,gamma","delta ""quoted"""'
  );
});

test('normalizers handle whitespace and chromosome prefixes', () => {
  assert.equal(converter.normalizeAllele(' a '), 'A');
  assert.equal(converter.normalizeChromosome('chr19'), '19');
  assert.equal(converter.normalizeChromosome(' mt '), 'MT');
});

test('chromosomeSortValue orders numeric and special chromosomes', () => {
  assert.equal(converter.chromosomeSortValue('1'), 1);
  assert.equal(converter.chromosomeSortValue('X'), 23);
  assert.equal(converter.chromosomeSortValue('MT'), 26);
  assert.equal(converter.chromosomeSortValue('weird'), 100);
});

test('parseDNAEraFile extracts metadata and rows', () => {
  const parsed = converter.parseDNAEraFile(sampleInput);
  assert.equal(parsed.metadata['GSGT Version'], '2.0.5');
  assert.equal(parsed.metadata.Content, 'GSAMD-24v3-0-EA_20034606_A1.bpm');
  assert.equal(parsed.rows.length, 11);
  assert.equal(parsed.rows[0]['SNP Name'], '1:103380393');
  assert.match(parsed.metadataLines[0], /^GSGT Version/);
});

test('resolveRsid handles name, GSA alias, direct rs and coordinate fallback', () => {
  assert.deepEqual(
    converter.resolveRsid({ 'SNP Name': 'rs1801133', Chr: '1', Position: '11856378' }, mapping),
    { rsid: 'rs1801133', mode: 'name', key: 'rs1801133' }
  );
  assert.deepEqual(
    converter.resolveRsid({ 'SNP Name': 'GSA-rs234706', Chr: '21', Position: '44485350' }, mapping),
    { rsid: 'rs234706', mode: 'name', key: 'GSA-rs234706' }
  );
  assert.deepEqual(
    converter.resolveRsid({ 'SNP Name': 'GSA-rs601338', Chr: '19', Position: '49206674' }, mapping),
    { rsid: 'rs601338', mode: 'name', key: 'rs601338' }
  );
  assert.deepEqual(
    converter.resolveRsid({ 'SNP Name': '19:45411941', Chr: '19', Position: '45411941' }, mapping),
    { rsid: 'rs429358', mode: 'coord', key: '19:45411941' }
  );
  assert.equal(
    converter.resolveRsid({ 'SNP Name': '1:110229816_CNV_GSTM1', Chr: '1', Position: '110229816' }, mapping),
    null
  );
});

test('buildOutput emits 23andMe-style text and tracks stats', () => {
  const parsed = converter.parseDNAEraFile(sampleInput);
  const result = converter.buildOutput(parsed, mapping);

  assert.match(result.text, /generated by 23andMe/);
  assert.match(result.text, /Original content: GSAMD-24v3-0-EA_20034606_A1\.bpm/);
  assert.match(result.text, /^rsid\tchromosome\tposition\tgenotype/m);
  assert.equal(result.matchedEntries.length, 8);
  assert.equal(result.stats.directName, 4);
  assert.equal(result.stats.coordinateFallback, 4);
  assert.equal(result.stats.skippedNoCall, 1);
  assert.equal(result.stats.skippedUnmapped, 2);
  assert.ok(result.missingTargets.includes('rs8175347'));
  assert.deepEqual(
    result.matchedEntries.map(entry => entry.rsid),
    ['rs1801133', 'rs953413', 'rs1800562', 'rs1799941', 'rs429358', 'rs7412', 'rs601338', 'rs234706']
  );
});

test('buildFilteredInputSubset keeps original DNAEra shape for relevant rows', () => {
  const parsed = converter.parseDNAEraFile(sampleInput);
  const filtered = converter.buildFilteredInputSubset(parsed, mapping);

  assert.equal(filtered.relevantRows.length, 8);
  assert.match(filtered.text, /^\[Header\]/);
  assert.match(filtered.text, /^Sample Name,SNP Name,Chr,Position,Allele1 - Plus,Allele2 - Plus$/m);
  assert.match(filtered.text, /^41220311706341,19:45411941,19,45411941,T,C$/m);
  assert.match(filtered.text, /^41220311706341,19:49206674,19,49206674,G,A$/m);
  assert.doesNotMatch(filtered.text, /^41220311706341,1:110229816_CNV_GSTM1,1,110229816,-,-$/m);
  assert.ok(filtered.text.includes('[Header]\nGSGT Version,2.0.5'));
});

